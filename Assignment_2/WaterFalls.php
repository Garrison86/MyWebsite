            <h2 style="text-align: left;">Helmcken Falls - Wells Gray Provincial Park, British Columbia</h2>
            <p style="font-size: 20px">One of the most beautiful waterfalls in Canada, Helmcken Falls are located 
            in the majestic Wells Gray Provincial Park, which was created in part to protect the powerful falls. 
            Tumbling water has gradually created a large canyon at the base of the falls and in the winter frozen 
            spray creates an icy wonderland along the edges. Easily accessible by road, there is a platform available 
            for getting that perfect shot.</p>
            <p style="font-size: 20px">A bit of Canadian waterfall trivia: Despite being named after John Sebastian 
            Helmcken for his role in bringing British Columbia into confederation, the physician passed away without 
            ever visiting the natural wonder.</p>
