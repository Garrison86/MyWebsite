        <div style="height: fit-content; margin: 20px;">
            <h2 style="text-align: left;">Pissing Mare Falls - Gros Morne National Park, Newfoundland and Labrador </h2>
            <p style="font-size: 20px">Pissing Mare Falls may have a funny-sounding name but they're no joke: in fact, they're 
            amongst the highest waterfalls in Canada. Located in the stunning Gros Morne National Park, the falls are only accessible 
            by boat tour and most of the time the pristine area is pretty much left alone. The falls drop into Western Brook Pond, 
            which is actually a fresh water fjord carved out by glaciers thousands of years ago. Towering cliffs covered with emerald 
            green foliage provide a dramatic backdrop to this natural phenomenon, making these falls a site to remember.
            </p>
        </div>
        <br>
        <div style="height: fit-content; margin: 20px;">
            <h2 style="text-align: left;">Shannon Falls - Shannon Falls Provincial Park, British Columbia</h2>
            <p style="font-size: 20px">One of the most beautiful waterfalls in Canada is Shannon Falls, whose 
            tumbling rapids rush over a series of granite cliffs into the Howe Sound and eventually the Pacific 
            Ocean. Easy to access and just two kilometres south of Squamish along the Sea to Sky Highway, 
            getting to these falls is a breeze. Best viewed in the spring or autumn, the falls are only 200 
            metres from the parking lot along a paved path. Once you arrive at the falls, a series of trails 
            and boardwalks allow you to further explore the pretty surroundings. Named after William Shannon, 
            a brick maker who settled in the area in the late 19th century, these falls are the third highest 
            in the province.</p>
           
        </div>
        <br>
        <div style="height: fit-content; margin: 20px;">
            <h2 style="text-align: left;">Niagara Falls - Niagara Falls, Ontario</h2>
            <p style="font-size: 20px">Niagara Falls are the most famous waterfalls in Canada—and understandably 
            so. Actually made up of three different falls, the aptly named Horseshoe Falls is the only one located 
            on the Canadian side. Easily accessible and always awe-inspiring, Horseshoe Falls is the most powerful 
            in North America. Receding glaciers formed the falls thousands of years ago and the beautiful green 
            colour is due to dissolved salts and finely ground rock. Multiple viewing options are available 
            including the Hornblower boat cruise. The Hornblower replaced the Maid of Mist tour boat, which 
            operated from 1846-2013 and was North America's oldest tourist attraction. In 2007, the base of the 
            falls was used to portray “World's End” in the movie Pirates of the Caribbean: At World's End.</p>
