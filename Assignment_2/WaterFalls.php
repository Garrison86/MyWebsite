        <div style="height: fit-content; margin: 20px;">
            <h2 style="text-align: left;">Athabasca Falls - Jasper National Park, Alberta</h2>
            <p style="font-size: 20px">Athabasca Falls is easily one of the most beautiful waterfalls in Canada, and a 
            jewel in the crown of Alberta's legendary Jasper National Park. Cutting through rock and opening onto 
            sweeping mountain vistas, multiple routes and bridges allow for a variety of viewpoints</p>
            <p style="font-size: 20px">Before reaching the falls, you'll walk along the magnificent limestone gorge where fast flowing water has 
            carved away smooth formations leaving pretty little crevices and swirling whirlpools. Ranging from milky white to aqua 
            blue, the striking colour of the water changes throughout the year, but is always awe-inspiring.</p>
            <img src="images\travel\medium\falls2.png" style="height: 400px;" title="Photo: Shutterstock - Athabasca Falls - Jasper National Park, Alberta"></img>

